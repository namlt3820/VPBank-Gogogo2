      <h1 class="title">Đặc điểm sản phẩm</h1>
      <p><b>Các khái niệm cơ bản của sản phẩm cho vay có TSĐB:</b></p>
      <ul>
        <li>Khách hàng, người đồng vay, bảo lãnh:</li>
        <ul>
          <li>Khách hàng là người Việt Nam. Độ tuổi từ 18 đến dưới 75 tuổi. Vay kinh doanh nguồn thu từ HKD: Tối thiểu 25 tuổi tại thời điểm vay vốn</li>
          <li>Người đồng vay là người cùng đứng tên trong hợp đồng vay.</li>
        </ul>
        <li>Người thân, chủ sở hữu TSBĐ: Người thân trong gia đình, anh/chị em…Chấp nhận vợ/chồng là người nước ngoài.</li>
        <li>Nhóm nợ: Nếu vay ngân hàng trong quá trình trả nợ bạn không có khả năng thanh toán, hay trả chậm thì bị xem vào nợ quá hạn của ngân hàng. Hiện nay ngân hàng chia làm 5 nhóm nợ xấu tùy theo mức độ và thời gian thanh toán chậm:</li>
        <ul>
          <li>Nhóm 1: Dư nợ đủ tiêu chuẩn ( Không quá hạn từ 1 đến dưới 10 ngày)</li>
          <li>Nhóm 2: Nợ nần cần chú ý (Nợ từ 10 đến dưới 90 ngày)</li>
          <li>Nhóm 3: Dư nợ dưới tiêu chuẩn (trên 90 đến 180 ngày)</li>
          <li>Nhóm 4: Dư nợ có nghi ngờ (Từ 181 đến 360 ngày)</li>
          <li>Nhóm 5: Dư nợ có khả năng mất vốn (nợ quá hạn trên 360 ngày)</li>
        </ul>
        <li>LTV: Là từ viết tắt của cụm từ tiếng Anh Loan - To - Value. Đây là tỉ số giữa khoản vay trên giá trị tài sản đảm bảo do ngân hàng quy định.               
        <li>LTV: Là từ viết tắt của cụm từ tiếng Anh Loan - To - Value. Ví dụ, bạn định thế chấp ngôi nhà của mình để vay vốn, ngân hàng định giá ngôi nhà của bạn trị giá 1 tỷ đồng, và ngân hàng quy định LTV tối đa mà ngân hàng cho vay là 80% thì bạn sẽ được vay tối đa 800 triệu đồng.</li>
        <li>DTI: DTI là từ viết tắt của cụm từ tiếng Anh Debt – To – Income Ratio, có nghĩa là tỉ lệ giữa tổng số tiền trả nợ so với tổng thu nhập của khách hàng đi vay tiền ngân hàng. DTI là 1 thông số quan trọng mà bất kỳ ngân hàng nào cũng cần để quyết định có nên duyệt 1 khoản vay hay không? (Duyệt vay theo DTI góp phần giảm nợ xấu, giảm rủi ro cho phía ngân hàng). Ví dụ, bạn có thu nhập 20 triệu đồng 1 tháng, ngân hàng quy định DTI tối đa là 70% nghĩa là bạn chỉ có thể dùng tối đa 14 triệu để trả nợ hàng tháng. Nếu các chi phí bạn cần chi trong tháng quá 6 triệu còn lại, giả sử là 8 triệu bạn sẽ phải vay với thời hạn dài hơn hoặc số tiền ít hơn để đảm bảo số tiến trả hàng tháng không quá 20-8 = 12 triệu đồng nhé.</li>
      </ul>
